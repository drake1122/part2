-- [파츠 종류 분류] (기존)
--   ca_id: 5000=파츠루트, 5001=오일필터, 5002=에어필터 ...
--
-- [부품브랜드 분류] ← 신규 (이 파일)
--   ca_id: B0=부품브랜드(루트)
--   ca_id: B01=모빌 (엔진오일)
--   ca_id: B02=카스트롤
--   ca_id: B03=쉘
--   ca_id: B04=지크
--   ca_id: B05=OEM정품 (BMW)
--   ca_id: B06=OEM정품 (벤츠)
--   ca_id: B07=만(MAN)
--   ca_id: B08=발레오(Valeo)
--   ca_id: B09=보쉬(Bosch)
--   ca_id: B10=ZF
--   ca_id: B11=SKF
--   ca_id: B12=GATES
--   ca_id: B13=BREMBO
--   ca_id: B14=TRW
--   ca_id: B15=SACHS
--   ca_id: B16=FEBI
--   ca_id: B17=HENGST
--   ca_id: B18=MANN-FILTER
--   ca_id: B19=기타브랜드
--
-- ■ 상품 다중분류 사용방법 (g5_shop_item)
--   ca_id  = 주분류 (차종 또는 파츠종류 중 택1)
--   ca_id2 = 2차분류 (다른 차종 or 파츠종류 or 부품브랜드)
--   ca_id3 = 3차분류 (추가 차종 or 부품브랜드)
--
-- ■ 등록 예시
--   모빌1 엔진오일 5W-30:
--     ca_id  = '5008'  ← 파츠종류: 엔진오일
--     ca_id2 = 'B01'   ← 부품브랜드: 모빌
--     ca_id3 = ''
--   → 쇼핑몰에서 [파츠종류>엔진오일] 또는 [부품브랜드>모빌] 클릭 시 모두 표시
--
--   BMW 3시리즈 F30용 OEM 오일필터:
--     ca_id  = '2010'  ← 차종: BMW 3시리즈 F30
--     ca_id2 = '5001'  ← 파츠종류: 오일필터
--     ca_id3 = 'B05'   ← 부품브랜드: BMW OEM정품
--   → 차종별/부품종류별/브랜드별 3방향 검색 가능
--
-- =====================================================

